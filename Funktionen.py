import math

def addition(a, b):
    result = a + b
    return result

def subtraktion(a, b):
    result = a - b
    return result

def division(a, b):
    result = a / b
    return result

def multiplikation(a, b):
    result = a * b
    return result

def quadrat(a):
    return a * a


def wurzel(x):
    return math.sqrt(x)


def logarithmus(x):
    return math.log(x)
